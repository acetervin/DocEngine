export type TranslationKey = keyof typeof t.en;

export const currencySymbols: Record<string, string> = {
  KWD: 'د.ك',
  KES: 'KES',
  USD: '$',
};

export const formatCurrency = (amount: number, currency: string, lang: string = 'en') => {
  const sym = currencySymbols[currency] || currency;
  const formatted = amount.toLocaleString(lang === 'ar' ? 'ar-EG' : 'en-US', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
  return lang === 'ar' ? `${formatted} ${sym}` : `${sym} ${formatted}`;
};
